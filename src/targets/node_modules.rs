use std::path::Path;
use std::time::{Duration, SystemTime};

use anyhow::Result;
use walkdir::WalkDir;

use super::Target;
use crate::config::Config;
use crate::fsutil;
use crate::report::{CleanAction, Finding, Report};

pub struct NodeModules;

impl Target for NodeModules {
    fn name(&self) -> &'static str {
        "node-modules"
    }

    fn enabled(&self, cfg: &Config) -> bool {
        cfg.node_modules
    }

    fn scan(&self, cfg: &Config) -> Result<Report> {
        let mut report = Report::new(self.name());
        let stale = Duration::from_secs(cfg.node_modules_stale_days * 86_400);

        for root in &cfg.scan_roots {
            if !root.is_dir() {
                continue;
            }
            let mut walker = WalkDir::new(root).into_iter();
            while let Some(entry) = walker.next() {
                let entry = match entry {
                    Ok(e) => e,
                    Err(_) => continue,
                };
                if entry.file_type().is_dir() && entry.file_name() == "node_modules" {
                    let path = entry.path().to_path_buf();
                    let size = fsutil::dir_size(&path);
                    let note = parent_stale(&path, stale)
                        .then(|| format!("project idle > {}d", cfg.node_modules_stale_days));
                    report.findings.push(Finding {
                        path,
                        size,
                        note,
                        action: CleanAction::RemovePath,
                    });
                    walker.skip_current_dir();
                }
            }
        }
        report.findings.sort_by(|a, b| b.size.cmp(&a.size));
        Ok(report)
    }
}

fn parent_stale(node_modules: &Path, age: Duration) -> bool {
    let parent = match node_modules.parent() {
        Some(p) => p,
        None => return false,
    };
    let modified = match parent.metadata().and_then(|m| m.modified()) {
        Ok(t) => t,
        Err(_) => return false,
    };
    SystemTime::now()
        .duration_since(modified)
        .map(|elapsed| elapsed > age)
        .unwrap_or(false)
}
